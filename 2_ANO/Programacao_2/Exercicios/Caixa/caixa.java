package caixa;

public class caixa {
    double altura;
    double largura;
    double comprimento;
    double volume;

    public caixa(){
        this.altura = 0;
        this.largura = 0;
        this.comprimento = 0;
        this.volume = 0;
    }

    double calcularVolume(){
        return volume = comprimento * largura * altura;
    }
}
